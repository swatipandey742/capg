package com.cpg.day2;

public class monthswitchcase {
	public static void main(String[]args) {
		int month=12;
		switch(month) {
		case 1:
			System.out.println("month : jan");
			break ;
		case 2:
			System.out.println("month : feb");
			break;
		case 3:
			System.out.println("month : march");
			break;
		case 4:
			System.out.println("month : april");
			break;
		case 5:
			System.out.println("month : may");
			break;
		case 6:
			System.out.println("month : june");
			break;
		case 7:
			System.out.println("month : july");
			break;
		case 8:
			System.out.println("month : Aug");
			break;
		case 9:
			System.out.println("month : sep");
			break;
		case 10:
			System.out.println("month : oct");
			break;
		case 11:
			System.out.println("month : nov");
			break;
		case 12:
			System.out.println("month : dec");
			break;
		default:
			System.out.println("invalid month");
			break;
		}
	}

			
			

	}

